# min y max

menor = min(58,96,72,64,35)
mayor = max(58,96,72,64,35)
print(mayor)
print(menor)

# otra forma

lista = (58,96,72,64,35)

print(f"El menor es {min(lista)} y el mayor es {max(lista)}")


